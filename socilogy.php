                <li><strong>Human Resources Specialist:</strong> Managing employee relations and workplace dynamics.</li>
                <li><strong>Community Organizer:</strong> Coordinating and leading community initiatives.</li>
                <li><strong>Policy Analyst:</strong> Analyzing and advising on social policies.</li>
                <li><strong>Social Services Coordinator:</strong> Working with marginalized or vulnerable populations.</li>
