            <h2>Introduction to Bachelor's in Sociology</h2>
            <p>A Bachelor's in Sociology (BS Sociology) is a study of human societies, behavior, and interactions. This program explores various aspects of social life, providing insights into patterns, inequalities, and dynamics that shape communities and cultures.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>Eligibility criteria for pursuing a BS Sociology program generally include:</p>
            <ul>
                <li>Completion of high school or equivalent education from a recognized board.</li>
                <li>Meeting the required minimum scores in relevant entrance exams, depending on the institution and region.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Duration and Structure</h2>
            <p>The BS Sociology program typically spans over a duration of 3 to 4 years. The curriculum covers a wide range of sociological theories and research methods.</p>
        </section>
        <section id="subjects">
            <h2>Core Subjects in BS Sociology</h2>
            <p>The curriculum of BS Sociology covers a variety of subjects, including:</p>
            <ul>
                <li>Introduction to Sociology</li>
                <li>Social Theory</li>
                <li>Research Methods in Sociology</li>
                <li>Social Inequality</li>
                <li>Sociology of Gender</li>
                <li>Sociology of Race and Ethnicity</li>
                <li>Family and Marriage</li>
                <li>Social Institutions</li>
                <li>Globalization and Society</li>
                <li>Social Change and Development</li>
            </ul>
        </section>
        <section id="specializations">
            <h2>Specializations in BS Sociology</h2>
            <p>BS Sociology offers specialization options catering to specific areas within the field:</p>
            <ul>
                <li><strong>Urban Sociology:</strong> Studying urban environments, communities, and issues.</li>
                <li><strong>Medical Sociology:</strong> Analyzing health, illness, and healthcare systems.</li>
                <li><strong>Criminology:</strong> Exploring crime, deviance, and criminal justice systems.</li>
                <li><strong>Social Research:</strong> Focusing on research methods and data analysis.</li>
                <li><strong>Environmental Sociology:</strong> Studying the relationship between society and the environment.</li>
            </ul>
        </section>
        <section id="internships">
            <h2>Internships and Practical Experience</h2>
            <p>BS Sociology programs often incorporate internships, fieldwork, and practical experiences, allowing students to apply sociological concepts in real-world settings.</p>
        </section>
        <section id="significance">
            <h2>Significance of Studying Sociology</h2>
            <p>Studying sociology offers insights into various aspects of society:</p>
            <ul>
                <li><strong>Understanding Social Structures:</strong> Analyzing institutions, norms, and values.</li>
                <li><strong>Examining Inequality:</strong> Investigating social disparities based on class, gender, race, and more.</li>
                <li><strong>Social Change:</strong> Studying the processes that drive societal transformations.</li>
                <li><strong>Group Dynamics:</strong> Understanding how individuals interact within groups.</li>
                <li><strong>Cultural Diversity:</strong> Exploring cultural practices and identities.</li>
            </ul>
        </section>
        <section id="scope">
            <h2>Scope of BS Sociology</h2>
            <p>BS Sociology graduates can explore a variety of career opportunities:</p>
            <ul>
                <li><strong>Social Services:</strong> Working in social welfare organizations and NGOs.</li>
                <li><strong>Research and Analysis:</strong> Conducting sociological research in academic or applied settings.</li>
                <li><strong>Human Resources:</strong> Managing workforce diversity and employee well-being.</li>
                <li><strong>Community Development:</strong> Engaging in projects that enhance local communities.</li>
                <li><strong>Policy Analysis:</strong> Influencing public policies and decision-making.</li>
            </ul>
        </section>
