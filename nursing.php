            <h2>Introduction to Nursing</h2>
            <p>Nursing is a deeply fulfilling profession dedicated to providing compassionate and holistic care to individuals across the lifespan. Nurses are essential members of healthcare teams, working collaboratively with doctors, therapists, and other healthcare professionals to promote health, prevent illnesses, and provide direct patient care.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>To embark on a nursing career, aspiring nurses usually need to fulfill these eligibility criteria:</p>
            <ul>
                <li>Completion of high school or equivalent education with a strong foundation in science and mathematics.</li>
                <li>Completion of prerequisite courses, such as Biology, Chemistry, and English.</li>
                <li>Minimum required GPA or entrance exam scores, such as the National Council Licensure Examination (NCLEX) in the United States.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Program Duration</h2>
            <p>The duration of nursing programs varies based on the educational level pursued:</p>
            <ul>
                <li>Associate Degree in Nursing (ADN): Typically takes 2 to 3 years and focuses on essential nursing skills.</li>
                <li>Bachelor of Science in Nursing (BSN): Requires around 4 years and offers a comprehensive education in nursing theory and practice.</li>
                <li>Master of Science in Nursing (MSN): Takes 1 to 2 years for those with a BSN and offers advanced nursing specialization.</li>
                <li>Doctor of Nursing Practice (DNP): Requires around 3 to 4 years for those with a BSN and focuses on advanced clinical practice.</li>
            </ul>
        </section>
        <section id="cost">
            <h2>Cost and Financial Considerations</h2>
            <p>The cost of nursing education varies based on factors like program type, institution, and location. Students should consider tuition fees, textbooks, clinical supplies, and potential living expenses. Financial aid options such as scholarships, grants, and loans are available to help manage costs.</p>
        </section>
        <section id="specialization">
            <h2>Specialization Fields</h2>
            <p>Nursing offers a diverse array of specialization fields that cater to different healthcare needs:</p>
            <ul>
                <li>Medical-Surgical Nursing: Providing care to patients undergoing surgical procedures or with complex medical conditions.</li>
                <li>Pediatric Nursing: Caring for children and adolescents, addressing their unique medical and emotional needs.</li>
                <li>Obstetric and Gynecological Nursing: Specializing in women's health, including pregnancy, childbirth, and reproductive health.</li>
                <li>Psychiatric-Mental Health Nursing: Assisting individuals with mental health challenges through therapeutic interventions.</li>
                <li>Geriatric Nursing: Focusing on the health and well-being of elderly patients in various care settings.</li>
                <li>Critical Care Nursing: Providing intensive care to critically ill patients in settings like intensive care units (ICUs).</li>
                <li>Community Health Nursing: Delivering healthcare services to communities, promoting preventive care and health education.</li>
                <li>Nurse Educator: Educating future nurses and healthcare professionals in academic and clinical settings.</li>
                <li>Nurse Practitioner: Performing advanced assessments, diagnosing illnesses, and prescribing treatments under the supervision of a doctor.</li>
                <li>Nurse Anesthetist: Administering anesthesia during surgical procedures and managing patients' pain relief.</li>
                <li>Nurse Midwife: Providing care to expectant mothers during pregnancy, childbirth, and postpartum periods.</li>
                <li>Research Nurse: Contributing to healthcare research by participating in clinical trials and studies.</li>
            </ul>
        </section>
        <section id="jobs">
            <h2>Jobs and Career Opportunities</h2>
            <p>Nursing graduates have a wide range of career opportunities in diverse healthcare settings:</p>
            <ol>
                <li>Registered Nurse (RN): Providing patient care, administering medications, and collaborating with healthcare teams.</li>
                <li>Clinical Nurse Specialist: Offering specialized expertise in a specific area of healthcare practice.</li>
                <li>Nurse Practitioner (NP): Diagnosing and treating illnesses, ordering tests, and prescribing medications.</li>
                <li>Certified Nurse Midwife (CNM): Providing care to expectant mothers and assisting in childbirth.</li>
                <li>Certified Registered Nurse Anesthetist (CRNA): Administering anesthesia during surgical procedures.</li>
                <li>Pediatric Nurse: Caring for children and adolescents in various healthcare settings.</li>
                <li>Neonatal Nurse: Providing care to newborns, especially those born prematurely or with health issues.</li>
                <li>Oncology Nurse: Specializing in the care of patients with cancer, managing treatments and side effects.</li>
                <li>Psychiatric-Mental Health Nurse: Supporting individuals with mental health challenges and promoting emotional well-being.</li>
                <li>Geriatric Nurse: Caring for elderly patients and addressing their unique healthcare needs.</li>
                <li>Intensive Care Unit (ICU) Nurse: Providing specialized care to critically ill patients in intensive care settings.</li>
                <li>Emergency Room Nurse: Offering immediate care to patients in emergency situations.</li>
                <li>Home Health Nurse: Delivering care to patients in their homes, especially those with chronic illnesses.</li>
                <li>Public Health Nurse: Promoting community health, preventive care, and health education.</li>
                <li>Nurse Educator: Educating and mentoring future nurses in academic and clinical settings.</li>
                <li>Research Nurse: Contributing to healthcare research by coordinating and participating in clinical studies.</li>
            </ol>
