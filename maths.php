            <h2>Introduction to BS Mathematics</h2>
            <p>Bachelor of Science in Mathematics (BS Mathematics) is a program that focuses on the study of mathematical principles, concepts, and their applications in various fields. Mathematics serves as a foundation for solving real-world problems, developing theories, and advancing scientific knowledge.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>Eligibility criteria for pursuing a BS Mathematics program generally include:</p>
            <ul>
                <li>Completion of high school or equivalent education with a strong foundation in mathematics.</li>
                <li>Meeting the required minimum scores in relevant entrance exams, depending on the institution and region.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Duration and Structure</h2>
            <p>The BS Mathematics program typically spans over a duration of 3 to 4 years, depending on the curriculum and the institution's academic schedule. The program comprises a combination of classroom lectures, problem-solving sessions, and research projects.</p>
        </section>
        <section id="subjects">
            <h2>Core Subjects in BS Mathematics</h2>
            <p>The curriculum of BS Mathematics covers a wide range of subjects to provide a comprehensive understanding of mathematical theories and techniques. Some of the core subjects include:</p>
            <ul>
                <li>Calculus</li>
                <li>Linear Algebra</li>
                <li>Abstract Algebra</li>
                <li>Real Analysis</li>
                <li>Complex Analysis</li>
                <li>Differential Equations</li>
                <li>Probability and Statistics</li>
                <li>Numerical Analysis</li>
                <li>Discrete Mathematics</li>
                <li>Mathematical Logic</li>
            </ul>
        </section>
        <section id="specializations">
            <h2>Specializations in BS Mathematics</h2>
            <p>After completing a BS Mathematics degree, graduates can pursue various specializations based on their interests and career goals:</p>
            <ul>
                <li><strong>Applied Mathematics:</strong> Applying mathematical methods to solve practical problems in various fields.</li>
                <li><strong>Pure Mathematics:</strong> Focusing on mathematical theories, proofs, and abstract concepts.</li>
                <li><strong>Statistics:</strong> Analyzing and interpreting data to make informed decisions.</li>
                <li><strong>Operations Research:</strong> Applying mathematical optimization techniques to solve complex problems.</li>
                <li><strong>Actuarial Science:</strong> Analyzing financial risks and uncertainties using mathematical models.</li>
                <li><strong>Cryptography:</strong> Studying techniques for secure communication and data protection.</li>
                <li><strong>Mathematical Education:</strong> Preparing for a career in teaching mathematics at various education levels.</li>
                <li><strong>Computational Mathematics:</strong> Using computers to solve mathematical problems and simulations.</li>
            </ul>
        </section>
        <section id="scope">
            <h2>Scope of BS Mathematics</h2>
            <p>Graduates of BS Mathematics have a wide range of opportunities in various fields:</p>
            <ul>
                <li><strong>Education:</strong> Mathematics graduates can become educators, teaching mathematics in schools, colleges, and universities.</li>
                <li><strong>Research:</strong> Mathematicians can conduct research in universities, research institutions, and industries to develop new mathematical theories and techniques.</li>
                <li><strong>Finance and Banking:</strong> Graduates can work in quantitative analysis, risk assessment, and financial modeling.</li>
                <li><strong>Technology and Software:</strong> Mathematics plays a crucial role in software development, algorithms, and computer science.</li>
                <li><strong>Data Analysis and Statistics:</strong> Mathematics is essential for analyzing and interpreting data in various industries.</li>
                <li><strong>Actuarial Science:</strong> Graduates can work in insurance companies, analyzing risks and determining premiums.</li>
                <li><strong>Research and Development:</strong> Mathematics is used in scientific research and technological innovation.</li>
            </ul>
        </section>
