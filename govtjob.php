                <h2>Competitive Exams</h2>
                <p>Competitive exams are types of exams where applicants are tested and evaluated based on their<br> performance in Written Examination that consists of multiple compulsory and optional subjects .<br>
Competitive Exams are very common in Pakistan as well as in India, Bangladesh and Sri Lanka where students <br>prepare to win a Government Job through competitive written tests and panel interviews.<br> As in Pakistan, many Students are preparing competitive exams like CSS and PMS to become a Future Bureaucrats.</p>
                <p><h2> 1. CSS Exam in Pakistan</h2><br>
                    The most challenging examination in Pakistan, abbreviated as CSS, demands a minimum of four<br> months of rigorous preparation. Successfully clearing this competitive test is a noteworthy accomplishment.<br>
                    <b> CSS Exam Pattern& procedur</b><br>
                    
                    The examination consists of two phases: the written test and the interview. Those who clear the written test proceed <br>to the interview stage. After passing the interview, candidates begin a 7-8 month training program.<br>
                <b>The Eligibility criteria for CSS in Pakistan</b><br>
                One must have to carry a Bachelor’s degree to be eligible for CSS Exam.<br> 
The age limit to attempt CSS exam is a minimum of 21 years to 30 years maximum.<br> 
2 Years age relaxation is given to candidates who are disabled, are from <br>Gilgit Baltistan or Azad Kashmir, working in armed forces, or as a contract employee for the government.<br>
A domicile certificate is essential to apply for the CSS exam in Pakistan.<br>
Candidates married to a person of a different nationality are not<br> eligible to be appointed to any government work posts after they pass their CSS exam.<br>
                <h2>2 .PMS Exam in Pakistan</h2><br>
                The next top exam in Pakistan is PMS. It refers to the Provincial Management Service exam. This exam is not that different from CSS exams.<br> The only difference between them is that PMS is an exam on the provincial level while CSS is an exam on a Federal level.<br> 
                <b>PMS Exam Pattern Procedure</b><br>
                The PMS exams comprised of a written paper, an interview, and a viva.<br>
The PMS exam has a total of 1200 marks. The written part of<br> the competitive exam is of 1000 marks while the viva is of 200 marks.<br>
A candidate needs to get at least 600 marks to be qualified for the interview.<br> Moreover, in the interview discussion, only those candidates who pass with at least 50% marks are shortlisted.<br>
Every wrong answer in the objective part of the written paper, 0.25 are deducted.<br>
                <b>The Eligibility Criteria for PMS Exam</b><br>
                The age limit is 21 to 30 years old.<br> 
Candidates who are disabled have a ten years age relaxation.<br>
To register for the PMS exam, one must have completed at least 16 years of education with a 2nd division.<br> 
People with a domicile of Punjab province can apply.<br>
                    <h2>3. ASSESSMENT TESTS</h2><br>
                    
                    Assessment examinations are exams that gather information about the candidates' ability to make judgments using all they have learned.<br>
                <b>FPSC Test in Pakistan</b><br>
                FPSC refers to Federal Public Service Commission, and the responsibility of this Federal service is to appoint public servants for the Government of Pakistan. FPSC is the administration that conducts two competitive exams: 

FPSC General Recruitment Test<br>
CSS <br>
                <b>FPSC General Recruitment Test in Pakistan</b><br>
                FPSC conducts this General recruitment test to employ candidates for different job posts in <br>the Federal Department instead of the occupational groups after giving the CSS exam.<br>
                <b>Eligibility Criteria for FPSC General Recruitment Test </b><br>
                Different posts are announced by FPSC each year for recruitments. Therefore, each job's<br> eligibility criteria vary with the age limit, experience, and job requirements.<br> FPSC mentions these conditions of job posts in the advertisements.<br>

However, the average age limit is 21 to 30 years.<br>
                <b>PPSC Competitive Exam in Pakistan</b><br>
                PPSC conducts Punjab Public Service Competitive Examination, a provincial <br>level exam in Punjab, to recruit civil servants for the Government of Punjab.<br> 
                <h5>Eligibility Criteria of PPSC exam</h5><br>
                The average age limit is 21 to 37 years.<br>
Age limit relaxation of up to 45 years for all State or Central Government employees.<br>
Age limit relaxation of up to 42 years for Backward class people of Punjab.<br>
The number of chances to give this exam is three.<br>
Only the residents of Punjab province are eligible.<br>
Educational qualification required varies according to the job posts that PPSC mentions in the advertisements posted. <br>
                    <h2>4.ENTRANCE EXAMS</h2><br>
                    
                   Many universities take assessment test as an entrance exam. The reason being that these are<br> standardized tests that measure the aptitude of candidates who want to apply.<br> Not only this, but these assessment exams also are used to measure the various skills a <br>candidate has, for example, their analytical, mathematical, writing, and verbal skills.<br>

Types of entrance exams<br>

NTS<br>
SAT<br>
ECAT<br>
MDCAT<br>
                <b>NTS Exam in Pakistan</b><br>
                NTS, also known as National Aptitude Test, is a requirement for many universities for their <br>admissions. You need to clear this exam in order to apply any University where NTS Exam is <br>compulsory. There are mainly two kinds of NTS exams that take place from time to time<br>

                <b>NAT</b><br>
NAT tests are for students who want to take admission in universities or colleges for BACHELORS or MASTERS. Furthermore, the two kinds of NAT exams are NAT-1 and NAT-2 tests.<br>

GAT<br>

GAT tests are for students who want to take admission to get a POSTGRADUATE degree.<br>

NAT-1<br>

The NAT-1 test is for students who want to apply to a Bachelor's or a Master's program at a renowned university. There are six different categories of NAT-1 exam:<br>

NAT-IGS is for the General sciences<br>
NAT-ICS is for Computer sciences<br>
NAT-ICOM is for students of Commerce<br>
NAT-IM is for students who chose Pre-medical In FSC or A-Level<br>
Nat-IE is for students of pre-engineering<br>
NAT-IA is for students of Arts and Humanities group<br>
Now the procedure for this is that the students who belong to a particular subject group <br>(example, students who chose arts and humanities in FSC or A level) will register in the appropriate <br>category of NAT-1. In the registration form, all the types of NAT-1 given above are present there;<br> you have to choose the right one.<br>

It's has been made easy for students to take this test because it takes place twelve different times a year.<br>

Eligibility Criteria of NAT-1<br>

The eligibility criteria of this NTS test requires only those students who have completed 12 years of <br>education to register. So, this is for those students who want to apply for admission to a Bachelor's program.<br>
                <b>NAT-2 </b><br>
                NAT-2 is the second type of NTS test for students who want to take admission to a Master's program.<br> This test is for Universities or institutions associated with NTS or requires NAT-2 scores for admissions of students.<br>

There are five types of NAT-2 test. Candidates must choose the test type that is related to their field of study.<br>

NAT-IIA is for students of Social sciences or Art group<br>
NAT-IIP is for students of Physical Sciences<br>
NAT-IIM is for students of Management sciences<br>
NAT-IIB is for students of Biological sciences<br>
NAT-IIO is for students of Islamic or Oriental studies<br>
<h3>Eligibility Criteria of NAT-2</h3><br>
The eligibility criteria of NAT-2 state that any student registering for this test should have completed their Bachelor's degree or have 14 years of education.<br> 

Marking in NTS Exams in Pakistan<br>

As such, NTS has no particular passing marks percentage.<br> Nevertheless, it depends on what the average marks of students in one NTS exam are.  <br>
For example, if the average marks (the maximum marks students get giving the same exam) are 80%, the standard passing percentage set by NTS is 85%.<br> 
No negative marking policy is present in NTS exams in Pakistan.<br>
                <b>GAT EXAM in Pakistan</b><br>
                The GAT is otherwise known as the Graduate Assessment Test. It is a test for law students in <br>Pakistan to enroll as an Advocate conducted by the HEC (Higher Education Commission). <br>Moreover, for those students who want to apply for MPhil or MS and Ph.D. programs in a university.<br>

There are four types of GAT tests:<br>

GAT Subject: For students applying for admission in Ph.D. programs.<br>
GAT-General: For students who want to apply for admission in MPhil or MS programs.<br>
SEE Law GAT: For foreign law students to work as an Advocate in Pakistan.<br>
Law GAT: For Pakistani law students to apply as an Advocate in Pakistan.<br>
               <h3> About GAT Test</h3>
There are three chances for law students who want to give either Law GAT or SEE Law.<br>
The GAT score is eligible for two years.<br>
Students with 16 years of education can give this exam.<br>
                <b>SAT Test in Pakistan</b><br>
                SAT or the Scholastic Aptitude Test. Students who have recently graduated from high school <br>take this test for universities where taking this test is compulsory. <br>For example, for universities like LUMS, one must undergo the SAT exam to get admission.

If the university that you want to apply to does not have a requirement of SAT scores for high school students, <br>then you do not have to take this test. However, even if you do, then it gives you a plus point<br> for your admission. Therefore, this type of exam comes under the category of Entrance exams.<br>

               <h3> SAT Test Pattern & Procedure</h3><br>
If you are giving the SAT for the first time, then you need a minimum of 2-3<br> months to prepare for it. The SAT divides into three portions as mentioned below<br>

<h3>SAT Reading Part</h3><br>
It includes questions that test the candidates reading ability and skills as<br> well as their vocabulary and pronunciation. The total time required for this portion is at least 65 minutes.<br>

<h3>SAT Writing Part</h3><br>
It comprises questions that test the candidate's use of grammar and how well<br> they understand it. The total time required for this section is 35 minutes.<br>

<h3>SAT Math Part</h3><br>
It contains questions that test students' problem-solving skills and includes various sections of algebra,<br> geometry, and other mathematical problems. This part is almost 80 minutes longer<br> because it has 58 questions, which are more significant than other parts.<br>
                <b>ECAT Test in Pakistan</b><br>
ECAT or the Engineering College Admission Test is conducted every year in Punjab by the University of <br>Engineering and Technology (UET), Lahore. This test is essential for students who want<br> to take admission for a Bachelor's degree in Engineering. <br>

                <h3>Eligibility Criteria for ECAT in Pakistan</h3><br>
Students with at least 60% marks in F.Sc., B.Sc., or any other equivalent program are eligible.<br>
Must have Provisional Domicile.<br>
ECAT Exam Procedure and Marking Pattern<br>
This exam has only multiple-choice questions.<br>
100 MCQs, and the total time for this exam is 100 minutes.<br>
Each MCQ is of four marks.<br>
For every wrong answer, one mark is deducted.<br>
              <b>  MDCAT Test in Pakistan</b><br>
MDCAT or the Medical and Dental College Admission Test is conducted every year in Punjab by the University of <br>Health Sciences, Lahore. This test is a requirement for students who want to take admission in BDS and MBBS.<br>

                <h3>Eligibility Criteria for MDCAT in Pakistan</h3>
Students with at least 70% marks in F.Sc Pre-medical.<br>
 Must have Provisional Domicile.<br>
MDCAT Exam Procedure and Marking Pattern  <br>
MDCAT comprises of only MCQs.<br>
Each MCQ is of 1 mark, and the total score is 200.<br>
No negative marking for the wrong answer.<br>
                    <h2>5. Spoken Language or English Language Courses</h2><br>
                These language tests are for those whose native language is not English. They are also <br>commonly used as entrance exams for several foreign universities. Apart from this,<br> these tests are also for immigration purposes to countries like Canada and the United Kingdom.<br>

There are mainly two main spoken language tests that come under this category: -<br>

<h4>TOEFL</h4><br>
<h4>IELTS</h4><br>
                    
                   <b>TOEFL TEST</b>
<h3>There are two types of TOEFL tests</h3><br>

TOEFL-IBT: It is an online paper. The score is made based on the reading and the listening test.<br> 
TOEFL-PBT: Scoring on listening, writing, and reading.<br>
TOEFL Eligibility Criteria<br>
There is not any specific age limit to give the TOEFL test.<br>
                <h3>TOEFL Scores</h3><br>
The TOEFL scores are valid until the next two of taking the test.<br>
Passing marks in TOEFL are 525<br>
<h3>IELTS TEST</h3><br>
It is a necessary test for those people who want to immigrate. And just like TOEFL, it tests the proficiency and understanding of the English-speaking skills.<br>

<h3>IELTS Eligibility Criteria</h3><br>
The minimum age to give the test is 16 years.<br>
If you want to apply to Australia, Canada, the USA, UK or, New Zealand, then take the test.<br>

<h3>IELTS Bands</h3><br>
IELTS scores are also called band scores. These band scores are separately given to every part of the test, and they range from 1 to 9.<br>

To pass the total band score required in IELTS is a 6.0<br></p>
                    
