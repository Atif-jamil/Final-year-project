            <h2>Introduction to BS Chemistry</h2>
            <p>Bachelor of Science in Chemistry (BS Chemistry) is a program that focuses on the scientific study of matter, its properties, composition, and interactions. It explores the fundamental principles of chemistry and their applications in various industries and fields.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>Eligibility criteria for pursuing a BS Chemistry program generally include:</p>
            <ul>
                <li>Completion of high school or equivalent education with a strong foundation in chemistry, mathematics, and physics.</li>
                <li>Meeting the required minimum scores in relevant entrance exams, depending on the institution and region.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Duration and Structure</h2>
            <p>The BS Chemistry program typically spans over a duration of 3 to 4 years, depending on the curriculum and the institution's academic schedule. The program comprises a combination of classroom lectures, laboratory sessions, and research projects.</p>
        </section>
        <section id="subjects">
            <h2>Core Subjects in BS Chemistry</h2>
            <p>The curriculum of BS Chemistry covers a wide range of subjects to provide a comprehensive understanding of chemical principles and their applications. Some of the core subjects include:</p>
            <ul>
                <li>General Chemistry</li>
                <li>Organic Chemistry</li>
                <li>Inorganic Chemistry</li>
                <li>Physical Chemistry</li>
                <li>Analytical Chemistry</li>
                <li>Quantum Chemistry</li>
                <li>Instrumental Analysis</li>
                <li>Environmental Chemistry</li>
                <li>Chemical Kinetics</li>
                <li>Chemical Thermodynamics</li>
            </ul>
        </section>
        <section id="scope">
            <h2>Scope of BS Chemistry</h2>
            <p>Graduates of BS Chemistry have a wide range of opportunities in various fields:</p>
            <ul>
                <li><strong>Research:</strong> Chemists can conduct research in universities, research institutions, and industries to develop new materials, discover new reactions, and advance chemical knowledge.</li>
                <li><strong>Pharmaceuticals and Healthcare:</strong> Graduates can work on drug discovery, formulation, quality control, and research in pharmaceutical companies.</li>
                <li><strong>Chemical Industry:</strong> Chemists can work in sectors such as petrochemicals, polymers, agrochemicals, and materials manufacturing.</li>
                <li><strong>Environmental Sciences:</strong> Graduates can contribute to environmental monitoring, pollution control, and sustainable practices.</li>
                <li><strong>Education:</strong> Chemists can become educators, teaching chemistry in schools, colleges, and universities.</li>
                <li><strong>Food and Beverage Industry:</strong> Graduates can work on quality control and safety in food and beverage production.</li>
                <li><strong>Forensics:</strong> Chemists can analyze evidence in criminal investigations.</li>
                <li><strong>Government Agencies:</strong> Graduates can work in regulatory agencies for safety and quality control.</li>
            </ul>
        </section>
        <section id="careers">
            <h2>Career Opportunities</h2>
            <p>BS Chemistry graduates have diverse career options. Here are some potential job roles:</p>
            <ol>
                <li><strong>Chemist:</strong> Conducting chemical analyses, research, and experiments.</li>
                <li><strong>Pharmaceutical Researcher:</strong> Working on drug development and testing.</li>
                <li><strong>Chemical Engineer:</strong> Designing chemical processes and equipment.</li>
                <li><strong>Quality Control Analyst:</strong> Ensuring product quality and safety.</li>
                <li><strong>Environmental Chemist:</strong> Monitoring and analyzing environmental pollutants.</li>
                <li><strong>Science Educator:</strong> Teaching chemistry in schools and colleges.</li>
                <li><strong>Food and Beverage Analyst:</strong> Analyzing food and beverage products for quality and safety.</li>
                <li><strong>Materials Scientist:</strong> Developing new materials for various applications.</li>
                <li><strong>Research Assistant:</strong> Assisting in scientific research projects.</li>
                <li><strong>Government Regulatory Officer:</strong> Enforcing chemical safety regulations.</li>
            </ol>
