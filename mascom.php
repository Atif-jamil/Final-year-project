                <li><strong>Advertising Executive:</strong> Creating and implementing advertising campaigns.</li>
                <li><strong>Media Producer:</strong> Producing content for television, film, radio, or digital platforms.</li>
                <li><strong>Social Media Manager:</strong> Managing and curating social media presence for organizations.</li>
                <li><strong>Content Writer:</strong> Creating written content for websites, blogs, and marketing materials.</li>
                <li><strong>Event Planner:</strong> Organizing and coordinating events for businesses and organizations.</li>
                <li><strong>News Anchor:</strong> Presenting news stories on television or radio broadcasts.</li>
                <li><strong>Documentary Filmmaker:</strong> Creating informative and engaging documentaries.</li>
                <li><strong>Media Researcher:</strong> Analyzing media trends and audience preferences.</li>
            </ol>
