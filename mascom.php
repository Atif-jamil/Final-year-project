            <h2>Introduction to BS Mass Communication</h2>
            <p>Bachelor of Science in Mass Communication (BS Mass Communication) is a program that focuses on the study of various forms of media, communication theories, and techniques. It equips students with the skills to create, analyze, and disseminate information through different media channels.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>Eligibility criteria for pursuing a BS Mass Communication program generally include:</p>
            <ul>
                <li>Completion of high school or equivalent education from a recognized board.</li>
                <li>Meeting the required minimum scores in relevant entrance exams, depending on the institution and region.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Duration and Structure</h2>
            <p>The BS Mass Communication program typically spans over a duration of 3 to 4 years, depending on the curriculum and the institution's academic schedule. The program comprises a combination of classroom lectures, practical exercises, projects, and internships.</p>
        </section>
        <section id="subjects">
            <h2>Core Subjects in BS Mass Communication</h2>
            <p>The curriculum of BS Mass Communication covers a wide range of subjects to provide a comprehensive understanding of media and communication. Some of the core subjects include:</p>
            <ul>
                <li>Introduction to Mass Communication</li>
                <li>Media Ethics and Laws</li>
                <li>Journalism and Reporting</li>
                <li>Media Production</li>
                <li>Public Relations</li>
                <li>Advertising and Marketing</li>
                <li>Mass Communication Research</li>
                <li>Media Management</li>
                <li>Visual Communication</li>
                <li>Media Psychology</li>
            </ul>
        </section>
        <section id="specializations">
            <h2>Specializations in BS Mass Communication</h2>
            <p>After completing a BS Mass Communication degree, students can specialize in various areas of mass communication based on their interests and career goals:</p>
            <ul>
                <li><strong>Journalism:</strong> Reporting news and current events through print, broadcast, or online media.</li>
                <li><strong>Public Relations:</strong> Managing communication between organizations and the public to maintain a positive image.</li>
                <li><strong>Advertising:</strong> Creating and promoting advertising campaigns for products and services.</li>
                <li><strong>Digital Media Production:</strong> Producing digital content for online platforms, social media, and streaming services.</li>
                <li><strong>Broadcasting:</strong> Working in radio and television production, including hosting, scriptwriting, and production.</li>
                <li><strong>Media Management:</strong> Managing media organizations, departments, or projects.</li>
                <li><strong>Media Research:</strong> Conducting research to understand audience preferences and media effectiveness.</li>
                <li><strong>Film and TV Production:</strong> Producing and directing films, documentaries, and television shows.</li>
            </ul>
        </section>
        <section id="scope">
            <h2>Scope of BS Mass Communication</h2>
            <p>Graduates of BS Mass Communication have diverse opportunities in various fields:</p>
            <ul>
                <li><strong>Media Organizations:</strong> Graduates can work in newspapers, magazines, TV stations, radio stations, and online media outlets.</li>
                <li><strong>Advertising Agencies:</strong> Creating and implementing advertising campaigns for clients.</li>
                <li><strong>Public Relations Firms:</strong> Managing communication strategies for organizations and individuals.</li>
                <li><strong>Corporate Communications:</strong> Handling internal and external communications for businesses.</li>
                <li><strong>Digital Marketing:</strong> Creating and managing online marketing campaigns and social media presence.</li>
                <li><strong>Broadcasting:</strong> Hosting shows, reporting news, and producing content for radio and television.</li>
                <li><strong>Media Production Companies:</strong> Producing films, documentaries, videos, and multimedia content.</li>
                <li><strong>Journalism:</strong> Reporting news, features, and investigative stories for various media platforms.</li>
                <li><strong>Event Management:</strong> Organizing and promoting events for businesses and organizations.</li>
            </ul>
        </section>
        <section id="careers">
