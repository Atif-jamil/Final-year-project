            <p>Doctor of Physical Therapy (DPT) is a professional degree that prepares students to become skilled physical therapists, specializing in the evaluation and treatment of musculoskeletal and movement disorders.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>To pursue a DPT degree, you typically need to fulfill the following eligibility criteria:</p>
            <ul>
                <li>Successful completion of high school or equivalent education.</li>
                <li>Completion of pre-requisite coursework (Biology, Chemistry, Anatomy, etc.).</li>
                <li>Satisfactory scores in physical therapy entrance exams (varies by country and institution).</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Program Duration</h2>
            <p>The DPT program usually spans around 3-4 years, depending on the curriculum and any additional requirements like internships.</p>
        </section>
        <section id="cost">
            <h2>Cost and Financial Considerations</h2>
