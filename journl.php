        <section id="introduction">
            <h2>Introduction to Bachelor's in Journalism</h2>
            <p>A Bachelor's in Journalism (BS Journalism) aims to cultivate students' skills in reporting, writing, editing, and understanding the broader context of media in society. It prepares students for careers in news organizations, digital media, broadcasting, and more.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>Eligibility criteria for pursuing a BS Journalism program generally include:</p>
            <ul>
                <li>Completion of high school or equivalent education from a recognized board.</li>
                <li>Meeting the required minimum scores in relevant entrance exams, depending on the institution and region.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Duration and Structure</h2>
            <p>The BS Journalism program typically spans over a duration of 3 to 4 years. The curriculum encompasses a mix of theoretical knowledge and practical skills in journalism.</p>
        </section>
        <section id="subjects">
            <h2>Core Subjects in BS Journalism</h2>
            <p>The curriculum of BS Journalism touches upon various areas of media and communication, including:</p>
            <ul>
                <li>Introduction to Journalism</li>
                <li>News Writing and Reporting</li>
                <li>Media Ethics and Laws</li>
                <li>Feature Writing</li>
                <li>Editorial and Opinion Writing</li>
                <li>Broadcast Journalism</li>
                <li>Photojournalism</li>
                <li>Digital Media and Online Journalism</li>
                <li>Investigative Journalism</li>
                <li>Public Relations and Corporate Communication</li>
            </ul>
        </section>
        <section id="specializations">
            <h2>Specializations in BS Journalism</h2>
            <p>BS Journalism offers a number of specializations, catering to specific areas within the media sector:</p>
            <ul>
                <li><strong>Broadcast Journalism:</strong> Focusing on television and radio reporting.</li>
                <li><strong>Investigative Journalism:</strong> Delving deep into issues, uncovering facts and exposing matters of public concern.</li>
                <li><strong>Photojournalism:</strong> Using photographs to narrate a story or event.</li>
                <li><strong>Digital Journalism:</strong> Producing content for online platforms and understanding the dynamics of digital media.</li>
                <li><strong>Sports Journalism:</strong> Covering sports events, athletes, and related news.</li>
            </ul>
        </section>
        <section id="internships">
            <h2>Internships and Practical Experience</h2>
            <p>BS Journalism programs emphasize internships, workshops, and fieldwork. Students often collaborate with newspapers, television channels, radio stations, or digital platforms, gaining hands-on experience and understanding real-world media dynamics.</p>
        </section>
        <section id="significance">
            <h2>Significance of Studying Journalism</h2>
            <p>Journalism plays a crucial role in:</p>
            <ul>
                <li><strong>Informing the Public:</strong> Providing accurate information and keeping the public informed.</li>
                <li><strong>Acting as a Watchdog:</strong> Holding the powerful accountable through investigative pieces.</li>
                <li><strong>Storytelling:</strong> Narrating compelling stories that resonate with the audience.</li>
                <li><strong>Promoting Democracy:</strong> Ensuring that citizens are informed and can make educated decisions.</li>
                <li><strong>Connecting Societies:</strong> Bridging different cultures, communities, and viewpoints.</li>
            </ul>
        </section>
        <section id="scope">
            <h2>Scope of BS Journalism</h2>
            <p>With a BS Journalism degree, graduates have a wide spectrum of opportunities:</p>
            <ul>
                <li><strong>Media Houses:</strong> Working as a reporter, editor, or producer.</li>
                <li><strong>Freelancing:</strong> Contributing articles, photos, or videos to various publications or platforms.</li>
                <li><strong>Public Relations:</strong> Managing communication for corporations or individuals.</li>
                <li><strong>Digital Platforms:</strong> Engaging in blogging, podcasting, or content creation for websites.</li>
                <li><strong>Research and Academia:</strong> Pursuing further studies or teaching journalism.</li>
            </ul>
        </section>
        <section id="careers">
            <h2>Career Opportunities</h2>
            <p>BS Journalism graduates can pursue a myriad of roles in the media industry:</p>
            <ol>
                <li><strong>Reporter:</strong> Gathering news and presenting it to the public.</li>
                <li><strong>Editor:</strong> Overseeing content, ensuring its accuracy and relevance.</li>
                <li><strong>Photojournalist:</strong> Capturing impactful images for news or features.</li>
                <li><strong>Broadcast Journalist:</strong> Working for TV or radio stations, either behind the scenes or in front of the camera.</li>
                <li><strong>Content Writer:</strong> Producing content for websites, magazines, or corporate communications.</li>
                <li><strong>Public Relations Specialist:</strong> Handling communication and image for organizations or individuals.</li>
                <li><strong>Digital Media Specialist:</strong> Managing online content, SEO, and social media for organizations.</li>
            </ol>
