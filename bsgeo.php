            <h2>Introduction to Bachelor's in Geography</h2>
            <p>A Bachelor's in Geography (BS Geography) explores the physical, human, and environmental aspects of the world. It examines the spatial relationships between people and their surroundings, delving into topics such as landforms, ecosystems, cultures, and urban development.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>Eligibility criteria for pursuing a BS Geography program generally include:</p>
            <ul>
                <li>Completion of high school or equivalent education from a recognized board.</li>
                <li>Meeting the required minimum scores in relevant entrance exams, depending on the institution and region.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Duration and Structure</h2>
            <p>The BS Geography program typically spans over a duration of 3 to 4 years. The curriculum encompasses a broad range of geographical topics.</p>
        </section>
        <section id="subjects">
            <h2>Core Subjects in BS Geography</h2>
            <p>The curriculum of BS Geography covers a diverse array of subjects, including:</p>
            <ul>
                <li>Physical Geography</li>
                <li>Human Geography</li>
                <li>Geographical Information Systems (GIS)</li>
                <li>Cultural Geography</li>
                <li>Environmental Geography</li>
                <li>Cartography and Map Design</li>
                <li>Urban and Regional Planning</li>
                <li>Geopolitics</li>
                <li>Remote Sensing</li>
                <li>Fieldwork and Research Methods</li>
            </ul>
        </section>
        <section id="specializations">
            <h2>Specializations in BS Geography</h2>
            <p>BS Geography offers specialization options that enable students to focus on specific aspects of geography:</p>
            <ul>
                <li><strong>Physical Geography:</strong> Exploring landforms, climates, and natural processes.</li>
                <li><strong>Human Geography:</strong> Studying the relationships between people and their environments.</li>
                <li><strong>Environmental Management:</strong> Addressing environmental challenges and sustainability.</li>
                <li><strong>Urban Planning:</strong> Analyzing urban development and spatial organization.</li>
                <li><strong>Geospatial Technology:</strong> Focusing on GIS, remote sensing, and mapping.</li>
            </ul>
        </section>
        <section id="internships">
            <h2>Internships and Practical Experience</h2>
            <p>BS Geography programs often incorporate internships, fieldwork, and practical experiences, allowing students to apply geographic concepts in real-world scenarios.</p>
        </section>
        <section id="significance">
            <h2>Significance of Studying Geography</h2>
            <p>Studying geography offers insights into various crucial aspects:</p>
            <ul>
                <li><strong>Environmental Understanding:</strong> Addressing environmental challenges and conservation.</li>
                <li><strong>Urban Planning:</strong> Contributing to sustainable urban development.</li>
                <li><strong>Human-Centered Insights:</strong> Understanding cultural diversity and societal patterns.</li>
                <li><strong>Global Interconnectedness:</strong> Analyzing the interactions between regions and nations.</li>
                <li><strong>Disaster Management:</strong> Navigating natural and human-induced disasters.</li>
            </ul>
        </section>
