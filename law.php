        <section id="introduction">
            <h2>Introduction to Bachelor's in Law (LLB)</h2>
            <p>A Bachelor's in Law (LLB) is an undergraduate program designed to provide students with a comprehensive understanding of the legal system, principles, and practices. LLB is the foundational degree for individuals aspiring to become legal professionals or explore various career opportunities in law-related fields.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>Eligibility criteria for pursuing an LLB program generally include:</p>
            <ul>
                <li>Completion of high school or equivalent education from a recognized board.</li>
                <li>Meeting the required minimum scores in relevant entrance exams, depending on the institution and region.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Duration and Structure</h2>
            <p>The LLB program typically spans over a duration of 3 to 5 years, depending on the region and curriculum. The curriculum covers various aspects of law, including constitutional law, criminal law, contract law, and more.</p>
        </section>
        <section id="subjects">
            <h2>Core Subjects in LLB</h2>
            <p>The LLB curriculum covers a range of core subjects to provide a comprehensive legal education. Some of the core subjects include:</p>
            <ul>
                <li>Constitutional Law</li>
                <li>Criminal Law</li>
                <li>Tort Law</li>
                <li>Contract Law</li>
                <li>Property Law</li>
                <li>Family Law</li>
                <li>Administrative Law</li>
                <li>Commercial Law</li>
                <li>Environmental Law</li>
                <li>International Law</li>
            </ul>
        </section>
        <section id="specializations">
            <h2>Specializations in LLB</h2>
            <p>LLB programs offer opportunities for specialization in specific areas of law:</p>
            <ul>
                <li><strong>Criminal Law:</strong> Focusing on crimes, criminal procedures, and justice system.</li>
                <li><strong>Corporate Law:</strong> Dealing with legal aspects of business and corporate transactions.</li>
                <li><strong>Family Law:</strong> Covering marriage, divorce, adoption, and child custody matters.</li>
                <li><strong>Intellectual Property Law:</strong> Addressing patent, copyright, and trademark issues.</li>
                <li><strong>Environmental Law:</strong> Exploring legal aspects of environmental protection and sustainability.</li>
            </ul>
        </section>
        <section id="internships">
            <h2>Internships and Practical Training</h2>
            <p>LLB programs often include internships and practical training opportunities for students to gain real-world experience. Internships can be with law firms, legal departments, or advocacy organizations.</p>
        </section>
        <section id="scope">
            <h2>Scope of LLB</h2>
            <p>LLB graduates have a wide range of career opportunities in various sectors:</p>
            <ul>
                <li><strong>Law Firms:</strong> Practicing law in areas of specialization.</li>
                <li><strong>Judiciary:</strong> Becoming judges, magistrates, or legal officers.</li>
                <li><strong>Corporate Sector:</strong> Working as legal advisors, compliance officers, or in-house counsels.</li>
                <li><strong>Government Departments:</strong> Serving in legal and administrative roles.</li>
                <li><strong>Academia and Research:</strong> Teaching and conducting legal research.</li>
                <li><strong>Nonprofit Organizations:</strong> Advocacy and legal aid services.</li>
            </ul>
        </section>
        <section id="careers">
            <h2>Career Opportunities</h2>
            <p>LLB graduates can pursue various legal and law-related roles. Some potential career options include:</p>
            <ol>
                <li><strong>Advocate:</strong> Representing clients in court and providing legal advice.</li>
                <li><strong>Judge:</strong> Presiding over legal proceedings and making legal decisions.</li>
                <li><strong>Corporate Counsel:</strong> Advising companies on legal matters and compliance.</li>
                <li><strong>Legal Consultant:</strong> Providing expert legal advice to individuals and organizations.</li>
                <li><strong>Legal Researcher:</strong> Conducting research on legal issues and preparing legal documents.</li>
                <li><strong>Public Prosecutor:</strong> Representing the government in criminal cases.</li>
                <li><strong>Human Rights Advocate:</strong> Advocating for human rights and social justice.</li>
            </ol>
