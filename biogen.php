        <section id="overview">
            <h2>Program Overview</h2>
            <p>Bioinformatics and Genetics programs provide comprehensive training in both biology and computational techniques. Students learn how to analyze and interpret large biological datasets, including DNA sequences, protein structures, and gene expression profiles.</p>
            <p>These programs equip students with the skills to make meaningful contributions to fields like genomics, disease research, and drug development.</p>
        </section>
        <section id="cost">
