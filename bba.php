            <h2>Introduction to BBA</h2>
            <p>Bachelor of Business Administration (BBA) is a program that focuses on the study of business and management principles. It provides students with a foundational understanding of various aspects of business operations and management.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>Eligibility criteria for pursuing a BBA program generally include:</p>
            <ul>
                <li>Completion of high school or equivalent education from a recognized board.</li>
                <li>Meeting the required minimum scores in relevant entrance exams, depending on the institution and region.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Duration and Structure</h2>
            <p>The BBA program typically spans over a duration of 3 to 4 years, depending on the curriculum and the institution's academic schedule. The program comprises a combination of core business courses, elective courses, and practical projects.</p>
        </section>
        <section id="subjects">
            <h2>Core Subjects in BBA</h2>
            <p>The curriculum of BBA covers a wide range of subjects to provide a strong foundation in business and management. Some of the core subjects include:</p>
            <ul>
                <li>Principles of Management</li>
                <li>Marketing Management</li>
                <li>Financial Accounting</li>
                <li>Business Communication</li>
                <li>Human Resource Management</li>
                <li>Business Ethics and Corporate Governance</li>
                <li>Managerial Economics</li>
                <li>Business Law</li>
                <li>Organizational Behavior</li>
                <li>Quantitative Techniques for Business</li>
            </ul>
        </section>
        <section id="specializations">
            <h2>Specializations in BBA</h2>
            <p>While BBA itself is a general business program, some institutions offer specializations that students can choose based on their interests and career goals:</p>
            <ul>
                <li><strong>Marketing:</strong> Focusing on strategies to promote and sell products or services.</li>
                <li><strong>Finance:</strong> Dealing with financial management, investments, and analysis.</li>
                <li><strong>Human Resource Management:</strong> Managing personnel and employee-related matters.</li>
                <li><strong>Entrepreneurship:</strong> Developing skills to start and manage businesses.</li>
                <li><strong>Supply Chain Management:</strong> Managing the flow of goods and services.</li>
                <li><strong>International Business:</strong> Dealing with business operations on a global scale.</li>
            </ul>
        </section>
        <section id="allied-courses">
            <h2>Allied Courses</h2>
            <p>In addition to core subjects, BBA students may have the opportunity to take allied courses that complement their business education. Some common allied courses include:</p>
            <ul>
                <li>Business Analytics</li>
                <li>E-commerce</li>
                <li>Project Management</li>
                <li>Leadership and Team Management</li>
                <li>Business Strategy</li>
                <li>Financial Management</li>
                <li>Consumer Behavior</li>
                <li>International Marketing</li>
                <li>Human Resource Development</li>
                <li>Business Negotiation</li>
            </ul>
        </section>
        <section id="scope">
            <h2>Scope of BBA</h2>
            <p>Graduates of BBA have diverse opportunities in various industries and sectors:</p>
            <ul>
                <li><strong>Corporate Sector:</strong> Graduates can work in entry-level managerial roles in various departments such as marketing, finance, and human resources.</li>
                <li><strong>Entrepreneurship:</strong> Starting and managing their own businesses.</li>
                <li><strong>Consulting Firms:</strong> Providing advisory services to businesses.</li>
                <li><strong>Financial Institutions:</strong> Working in banks, investment firms, and insurance companies.</li>
                <li><strong>Nonprofit Organizations:</strong> Managing operations in nonprofit and social organizations.</li>
                <li><strong>Government Agencies:</strong> Working in government departments related to business and commerce.</li>
            </ul>
        </section>
