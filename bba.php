                <li><strong>Financial Analyst:</strong> Analyzing financial data and making investment decisions.</li>
                <li><strong>Human Resources Assistant:</strong> Assisting in recruitment and employee relations.</li>
                <li><strong>Operations Manager:</strong> Managing daily business operations.</li>
