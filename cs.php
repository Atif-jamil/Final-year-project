            <h2>Introduction to BSCS</h2>
            <p>Bachelor of Science in Computer Science (BSCS) is a program that focuses on the study of computer systems, algorithms, programming languages, and software development. It prepares students for careers in the rapidly evolving field of technology.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>Eligibility criteria for pursuing a BSCS program generally include:</p>
            <ul>
                <li>Completion of high school or equivalent education with a strong foundation in mathematics and science.</li>
                <li>Meeting the required minimum scores in relevant entrance exams, depending on the institution and region.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Duration and Structure</h2>
            <p>The BSCS program typically spans over a duration of 3 to 4 years, depending on the curriculum and the institution's academic schedule. The program comprises a combination of classroom lectures, lab work, and practical projects.</p>
        </section>
        <section id="subjects">
