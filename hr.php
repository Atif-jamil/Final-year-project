        <section id="introduction">
            <h2>Introduction to Bachelor's in Human Resources Management</h2>
            <p>A Bachelor's in Human Resources Management (HRM) is a program designed to equip students with the knowledge and skills required to manage an organization's workforce effectively. HR professionals play a crucial role in recruiting, training, developing, and retaining employees to enhance organizational performance.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>Eligibility criteria for pursuing a Bachelor's in HRM program generally include:</p>
            <ul>
                <li>Completion of high school or equivalent education from a recognized board.</li>
                <li>Meeting the required minimum scores in relevant entrance exams, depending on the institution and region.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Duration and Structure</h2>
            <p>The duration of a Bachelor's in HRM program typically ranges from 3 to 4 years. The curriculum covers a blend of human resources, management, and business-related subjects.</p>
        </section>
        <section id="subjects">
            <h2>Core Subjects in HRM</h2>
            <p>The curriculum of a Bachelor's in HRM covers a range of core subjects to provide a comprehensive understanding of human resources practices. Some of the core subjects include:</p>
            <ul>
                <li>Human Resource Management</li>
                <li>Organizational Behavior</li>
                <li>Employee Relations</li>
                <li>Recruitment and Selection</li>
                <li>Training and Development</li>
                <li>Compensation and Benefits</li>
                <li>Labor Law</li>
                <li>Strategic HRM</li>
                <li>Performance Management</li>
                <li>Conflict Resolution</li>
            </ul>
        </section>
        <section id="specializations">
            <h2>Specializations in HRM</h2>
            <p>Students pursuing a Bachelor's in HRM have the option to specialize in specific areas of human resources:</p>
            <ul>
                <li><strong>Recruitment and Selection:</strong> Focusing on identifying and hiring the right talent for an organization.</li>
                <li><strong>Training and Development:</strong> Developing training programs to enhance employee skills and performance.</li>
                <li><strong>Compensation and Benefits:</strong> Managing employee compensation, benefits, and rewards.</li>
                <li><strong>Employee Relations:</strong> Handling employee grievances, conflicts, and maintaining a positive work environment.</li>
                <li><strong>Strategic HRM:</strong> Aligning HR strategies with organizational goals and objectives.</li>
            </ul>
        </section>
        <section id="internships">
            <h2>Internships and Practical Experience</h2>
            <p>Bachelor's in HRM programs often include internships, projects, and practical experiences to provide students with real-world exposure to HR practices. Internships can be with HR departments, consulting firms, or organizations.</p>
        </section>
        <section id="scope">
            <h2>Scope of HRM</h2>
            <p>Graduates with a Bachelor's in HRM have diverse career opportunities across various industries:</p>
            <ul>
                <li><strong>Corporate Sector:</strong> HR professionals manage employee recruitment, training, and performance for companies.</li>
                <li><strong>Consulting Firms:</strong> Providing HR advisory and consulting services to organizations.</li>
                <li><strong>Government and Public Sector:</strong> HR roles in government departments, public institutions, and agencies.</li>
                <li><strong>Nonprofit Organizations:</strong> Managing human resources for nonprofit and charitable organizations.</li>
                <li><strong>Education and Healthcare:</strong> HR roles in educational institutions and healthcare organizations.</li>
            </ul>
        </section>
        <section id="careers">
            <h2>Career Opportunities</h2>
            <p>Graduates with a Bachelor's in HRM can pursue various roles in the field of human resources. Some potential career options include:</p>
            <ol>
                <li><strong>HR Generalist:</strong> Managing overall HR functions, including recruitment, training, and employee relations.</li>
                <li><strong>Recruitment Specialist:</strong> Focusing on sourcing and hiring qualified candidates for job openings.</li>
                <li><strong>Training and Development Coordinator:</strong> Designing and implementing employee training programs.</li>
                <li><strong>Compensation Analyst:</strong> Analyzing and managing employee compensation and benefits packages.</li>
                <li><strong>Employee Relations Manager:</strong> Handling employee conflicts, grievances, and maintaining a positive workplace culture.</li>
                <li><strong>HR Consultant:</strong> Providing expert HR advice to organizations and clients.</li>
                <li><strong>Organizational Development Specialist:</strong> Working to improve organizational effectiveness and efficiency.</li>
            </ol>
