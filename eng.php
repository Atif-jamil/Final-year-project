            <h2>Introduction to Bachelor's in English Literature</h2>
            <p>A Bachelor's in English Literature (BS English Literature) is a program that delves into the study of literary works from different time periods, cultures, and genres. This program focuses on critical analysis, interpretation, and appreciation of written expressions.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>Eligibility criteria for pursuing a BS English Literature program generally include:</p>
            <ul>
                <li>Completion of high school or equivalent education from a recognized board.</li>
                <li>Meeting the required minimum scores in relevant entrance exams, depending on the institution and region.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Duration and Structure</h2>
            <p>The BS English Literature program typically spans over a duration of 3 to 4 years. The curriculum covers a variety of literary texts, theories, and cultural contexts.</p>
        </section>
        <section id="subjects">
            <h2>Core Subjects in BS English Literature</h2>
            <p>The curriculum of BS English Literature covers a range of core subjects to provide a comprehensive understanding of literary analysis. Some of the core subjects include:</p>
            <ul>
                <li>Literary Theory and Criticism</li>
                <li>British Literature</li>
                <li>American Literature</li>
                <li>World Literature</li>
                <li>Shakespearean Studies</li>
                <li>Modernism and Postmodernism</li>
                <li>Gender Studies in Literature</li>
                <li>Creative Writing</li>
                <li>Literary Research Methods</li>
                <li>Literature and Film</li>
            </ul>
        </section>
        <section id="specializations">
            <h2>Specializations in BS English Literature</h2>
            <p>BS English Literature offers specialization options that allow students to focus on specific areas:</p>
            <ul>
                <li><strong>Medieval Literature:</strong> Studying literature from the Middle Ages.</li>
                <li><strong>Modern and Contemporary Literature:</strong> Exploring literature from the 19th to 21st centuries.</li>
                <li><strong>Postcolonial Literature:</strong> Analyzing literature from postcolonial contexts.</li>
                <li><strong>Gender and Sexuality in Literature:</strong> Examining representations of gender and sexuality.</li>
                <li><strong>Literary Translation:</strong> Learning about translation of literary works.</li>
            </ul>
        </section>
        <section id="internships">
            <h2>Internships and Practical Experience</h2>
            <p>BS English Literature programs often include internships, projects, and practical experiences to provide students with real-world exposure. Internships can be with publishing houses, media companies, cultural institutions, or educational organizations.</p>
        </section>
        <section id="scope">
            <h2>Scope of BS English Literature</h2>
            <p>BS English Literature graduates have diverse career opportunities in various fields:</p>
            <ul>
                <li><strong>Educational Institutions:</strong> Teaching literature at schools and colleges.</li>
                <li><strong>Publishing Industry:</strong> Editing, proofreading, and content creation.</li>
                <li><strong>Media and Journalism:</strong> Writing, reporting, and content development.</li>
                <li><strong>Cultural Organizations:</strong> Curating literary events and programs.</li>
                <li><strong>Research and Academia:</strong> Pursuing advanced degrees and research.</li>
            </ul>
        </section>
