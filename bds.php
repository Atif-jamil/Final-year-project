            <h2>Introduction to BDS</h2>
            <p>BDS (Bachelor of Dental Surgery) is a professional degree that prepares students for a career in dentistry and oral health care.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>To pursue a BDS degree, you typically need to fulfill the following eligibility criteria:</p>
            <ul>
                <li>Successful completion of high school or equivalent education.</li>
                <li>Completion of required pre-dental subjects (Biology, Chemistry, and Physics).</li>
                <li>Satisfactory scores in dental entrance exams (varies by country and institution).</li>
            </ul>
        </section>
        <section id="program-details">
            <h2>Program Details</h2>
            <p>The BDS program usually spans around 4-5 years and includes both theoretical study and practical clinical training. Students learn various dental procedures, oral health diagnostics, and patient care.</p>
        </section>
        <section id="specialization">
            <h2>Specialization Fields</h2>
            <p>After completing BDS, you can specialize in various dental fields through postgraduate education. Some common specialization fields include:</p>
            <ul>
                <li>Orthodontics</li>
                <li>Oral and Maxillofacial Surgery</li>
                <li>Prosthodontics</li>
                <li>Periodontics</li>
                <li>Endodontics</li>
                <li>Oral Pathology</li>
                <li>Pediatric Dentistry</li>
            </ul>
        </section>
