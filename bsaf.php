            <p>BS Accounting and Finance offers specialization options that allow students to focus on specific areas:</p>
            <ul>
                <li><strong>Financial Accounting:</strong> Concentrating on accounting principles, financial statements, and reporting.</li>
                <li><strong>Corporate Finance:</strong> Exploring financial decision-making, capital structure, and investment strategies.</li>
                <li><strong>Taxation:</strong> Focusing on tax laws, regulations, and strategies for individuals and corporations.</li>
                <li><strong>Investment Banking:</strong> Studying financial markets, investment analysis, and mergers and acquisitions.</li>
                <li><strong>Forensic Accounting:</strong> Learning to detect and prevent financial fraud and irregularities.</li>
            </ul>
