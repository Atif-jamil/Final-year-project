            <h2>Introduction to Bachelor's in International Relations</h2>
            <p>A Bachelor's in International Relations (BS IR) delves into the complexities of international politics, economics, and diplomacy. This course explores the interplay of nations, non-state actors, global institutions, and multinational corporations in shaping the global landscape.</p>
        </section>
        <section id="eligibility">
            <h2>Eligibility Criteria</h2>
            <p>Eligibility criteria for pursuing a BS IR program generally include:</p>
            <ul>
                <li>Completion of high school or equivalent education from a recognized board.</li>
                <li>Meeting the required minimum scores in relevant entrance exams, depending on the institution and region.</li>
            </ul>
        </section>
        <section id="duration">
            <h2>Duration and Structure</h2>
            <p>The BS IR program typically spans over a duration of 3 to 4 years. The curriculum covers political theory, international economics, international law, and other relevant subjects.</p>
        </section>
        <section id="subjects">
            <h2>Core Subjects in BS International Relations</h2>
            <p>The curriculum of BS IR encompasses a wide spectrum of subjects, such as:</p>
            <ul>
                <li>Introduction to International Relations</li>
                <li>World History</li>
                <li>International Political Economy</li>
                <li>International Law</li>
                <li>Comparative Politics</li>
                <li>Diplomatic Studies</li>
                <li>Conflict Resolution</li>
                <li>Regional Studies</li>
                <li>Global Security</li>
                <li>Foreign Policy Analysis</li>
            </ul>
        </section>
        <section id="specializations">
            <h2>Specializations in BS International Relations</h2>
            <p>BS IR offers specialization options catering to specific areas of global interest:</p>
            <ul>
                <li><strong>Global Security:</strong> Focusing on issues like terrorism, nuclear proliferation, and cyber threats.</li>
                <li><strong>International Development:</strong> Exploring the economic, social, and political aspects of developing nations.</li>
                <li><strong>Human Rights:</strong> Investigating global human rights issues and laws.</li>
                <li><strong>Environmental Policy:</strong> Studying global environmental challenges and strategies.</li>
                <li><strong>International Trade and Economics:</strong> Understanding global economic dynamics and trade policies.</li>
            </ul>
        </section>
        <section id="internships">
            <h2>Internships and Practical Experience</h2>
            <p>BS IR programs often encompass internships, fieldwork, and practical experiences, giving students real-world exposure to international institutions, NGOs, or foreign policy establishments.</p>
        </section>
        <section id="significance">
            <h2>Significance of Studying International Relations</h2>
            <p>International Relations provides valuable insights into:</p>
            <ul>
                <li><strong>Global Diplomacy:</strong> Understanding the intricacies of diplomatic ties and international negotiations.</li>
                <li><strong>World Economy:</strong> Gaining knowledge of international trade, finance, and development.</li>
                <li><strong>Global Security:</strong> Studying the dynamics of global threats and security measures.</li>
                <li><strong>Human Rights:</strong> Advocating for and understanding global human rights standards and issues.</li>
                <li><strong>Environmental Cooperation:</strong> Assessing global cooperation on environmental challenges.</li>
            </ul>
        </section>
        <section id="scope">
            <h2>Scope of BS International Relations</h2>
            <p>BS IR graduates find a plethora of opportunities in:</p>
            <ul>
                <li><strong>International Organizations:</strong> Working in bodies like the UN, WHO, or IMF.</li>
                <li><strong>Non-Governmental Organizations:</strong> Engaging in international development, human rights advocacy, or environmental initiatives.</li>
                <li><strong>Diplomatic Service:</strong> Representing and safeguarding national interests abroad.</li>
                <li><strong>Research and Think Tanks:</strong> Conducting studies on global trends and policy analysis.</li>
                <li><strong>Journalism:</strong> Covering international news and events.</li>
            </ul>
        </section>
        <section id="careers">
            <h2>Career Opportunities</h2>
            <p>BS IR graduates can take on diverse roles in the international landscape:</p>
            <ol>
                <li><strong>Foreign Service Officer:</strong> Engaging in diplomatic activities and foreign policy implementation.</li>
                <li><strong>International Development Worker:</strong> Working on development projects worldwide.</li>
                <li><strong>Policy Analyst:</strong> Researching and recommending policy decisions.</li>
                <li><strong>International Trade Specialist:</strong> Navigating the complexities of international trade regulations and treaties.</li>
                <li><strong>Global Affairs Journalist:</strong> Reporting on international events, crises, and developments.</li>
                <li><strong>Researcher:</strong> Undertaking comprehensive studies on international trends and relations.</li>
                <li><strong>Human Rights Advocate:</strong> Working for the protection and promotion of global human rights.</li>
            </ol>
